-- ============================================================================
-- 0157: Cover every foreign key with an index (scalability)
-- ----------------------------------------------------------------------------
-- Postgres does NOT auto-index FK columns. Unindexed FKs cause (a) sequential
-- scans on joins/filters and (b) slow parent DELETEs (each delete seq-scans the
-- child for referencing rows) — a primary bottleneck at scale. Adds a covering
-- index for all 112 FK columns flagged by the performance advisor. All
-- IF NOT EXISTS (idempotent). Instant on current sizes; for FUTURE large tables
-- prefer CREATE INDEX CONCURRENTLY (outside a transaction). Reversible (DROP).
-- ============================================================================
